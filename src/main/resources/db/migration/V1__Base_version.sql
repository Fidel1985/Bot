CREATE TABLE `ORDERS`
(
`ID` BIGINT NOT NULL,
`CONVERSE_ID` BIGINT,
`PAIR` VARCHAR(50) NOT NULL,
`OPERATION` VARCHAR(50) NOT NULL,
`AMOUNT` DOUBLE NOT NULL,
`PRICE` DECIMAL NOT NULL,
`CONVERSE_PRICE`DECIMAL,
`CREATE_DATE` DATETIME,
`DONE_DATE` DATETIME,
`CLOSE_DATE` DATETIME,
`CLOSED` BOOL NOT NULL,
`SPREAD` DECIMAL,
`PROFIT` DECIMAL,
CONSTRAINT `PK_ORDERS` PRIMARY KEY (`ID`)
);